void D3des(uchar inblock[8], uchar outblock[8],
			void *ksa) {
	ulong fval, work, right, leftt;
	ulong *keys = (ulong *)ksa;
	int round, iterate;
	
	leftt	= ((ulong)inblock[0] << 24)
            | ((ulong)inblock[1] << 16)
            | ((ulong)inblock[2] << 8)
            |  (ulong)inblock[3];
    right	= ((ulong)inblock[4] << 24)
            | ((ulong)inblock[5] << 16)
            | ((ulong)inblock[6] << 8)
            |  (ulong)inblock[7];
	work = ((leftt >> 4) ^ right) & 0x0f0f0f0fL;
	right ^= work;
	leftt ^= (work << 4);
	work = ((leftt >> 16) ^ right) & 0x0000ffffL;
	right ^= work;
	leftt ^= (work << 16);
	work = ((right >> 2) ^ leftt) & 0x33333333L;
	leftt ^= work;
	right ^= (work << 2);
	work = ((right >> 8) ^ leftt) & 0x00ff00ffL;
	leftt ^= work;
	right ^= (work << 8);
	right = ((right << 1) | ((right >> 31) & 1L)) & 0xffffffffL;
	work = (leftt ^ right) & 0xaaaaaaaaL;
	leftt ^= work;
	right ^= work;
	leftt = ((leftt << 1) | ((leftt >> 31) & 1L)) & 0xffffffffL;
	iterate = 1;
	goto Des0;
	while( iterate < 3 ) {
		work = right;
		right = leftt;
		leftt = work;
		iterate++;
Des0:	
		for( round = 0; round < 8; round++ ) {
			work  = ((right << 28) | (right >> 4)) ^ *keys++;
			fval  = SP7[ work		 & 0x3fL];
			fval |= SP5[(work >>  8) & 0x3fL];
			fval |= SP3[(work >> 16) & 0x3fL];
			fval |= SP1[(work >> 24) & 0x3fL];
			work  = right ^ *keys++;
			fval |= SP8[ work		 & 0x3fL];
			fval |= SP6[(work >>  8) & 0x3fL];
			fval |= SP4[(work >> 16) & 0x3fL];
			fval |= SP2[(work >> 24) & 0x3fL];
			leftt ^= fval;
			work  = ((leftt << 28) | (leftt >> 4)) ^ *keys++;
			fval  = SP7[ work		 & 0x3fL];
			fval |= SP5[(work >>  8) & 0x3fL];
			fval |= SP3[(work >> 16) & 0x3fL];
			fval |= SP1[(work >> 24) & 0x3fL];
			work  = leftt ^ *keys++;
			fval |= SP8[ work		 & 0x3fL];
			fval |= SP6[(work >>  8) & 0x3fL];
			fval |= SP4[(work >> 16) & 0x3fL];
			fval |= SP2[(work >> 24) & 0x3fL];
			right ^= fval;
			}
		}
	right = (right << 31) | (right >> 1);
	work = (leftt ^ right) & 0xaaaaaaaaL;
	leftt ^= work;
	right ^= work;
	leftt = (leftt << 31) | (leftt >> 1);
	work = ((leftt >> 8) ^ right) & 0x00ff00ffL;
	right ^= work;
	leftt ^= (work << 8);
	work = ((leftt >> 2) ^ right) & 0x33333333L;
	right ^= work;
	leftt ^= (work << 2);
	work = ((right >> 16) ^ leftt) & 0x0000ffffL;
	leftt ^= work;
	right ^= (work << 16);
	work = ((right >> 4) ^ leftt) & 0x0f0f0f0fL;
	leftt ^= work;
	right ^= (work << 4);
    outblock[0] = (uchar)(right >> 24) & 0xFF;
    outblock[1] = (uchar)(right >> 16) & 0xFF;
    outblock[2] = (uchar)(right >>  8) & 0xFF;
    outblock[3] = (uchar)(right      ) & 0xFF;
    outblock[4] = (uchar)(leftt >> 24) & 0xFF;
    outblock[5] = (uchar)(leftt >> 16) & 0xFF;
    outblock[6] = (uchar)(leftt >>  8) & 0xFF;
    outblock[7] = (uchar)(leftt      ) & 0xFF;
	return;
	}

